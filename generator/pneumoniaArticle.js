function randomNumber(num) {
    const ary = [];					//创建一个空数组用来保存随机数组
    for (let i = 0; i < num; i++) {			//按照正常排序填充数组
        ary[i] = i;
    }
    ary.sort(function () {
        return 0.5 - Math.random();		//返回随机正负值
    });
    return ary;					//返回数组
}